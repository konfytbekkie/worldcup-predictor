-- Implements SPEC.md invariants I1–I7. Idempotent-ish: drops views/functions first.

-- ---------------------------------------------------------------- tables

create table if not exists players (
  id   smallint generated by default as identity primary key,
  name text not null unique
);

create table if not exists matches (
  id           bigint primary key,          -- football-data.org match id
  matchday     int,
  stage        text not null,               -- GROUP_STAGE / LAST_32 / ... / FINAL
  grp          text,                        -- GROUP_A..GROUP_L (null in knockouts)
  kickoff_utc  timestamptz not null,
  status       text not null default 'TIMED',
  home_team    text,
  home_tla     text,
  home_crest   text,
  away_team    text,
  away_tla     text,
  away_crest   text,
  home_score   int,                         -- 90-minute (regulation) score
  away_score   int,
  last_synced  timestamptz not null default now()
);

create table if not exists predictions (
  id         bigint generated by default as identity primary key,
  player_id  smallint not null references players (id),
  match_id   bigint not null references matches (id),
  home_pred  int not null check (home_pred between 0 and 20),
  away_pred  int not null check (away_pred between 0 and 20),
  updated_at timestamptz not null default now(),
  unique (player_id, match_id)              -- I1
);

create table if not exists bonus_picks (
  player_id    smallint primary key references players (id),
  champion     text,
  golden_boot  text,
  bafana_stage text check (bafana_stage in
    ('GROUP','LAST_32','LAST_16','QUARTER_FINALS','SEMI_FINALS','RUNNER_UP','CHAMPIONS')),
  updated_at   timestamptz not null default now()
);

-- single row, filled by admin after the final
create table if not exists bonus_results (
  id           boolean primary key default true check (id),
  champion     text,
  golden_boot  text,
  bafana_stage text
);

create index if not exists idx_predictions_match on predictions (match_id);
create index if not exists idx_matches_kickoff on matches (kickoff_utc);

